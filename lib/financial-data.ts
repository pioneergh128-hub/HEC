export function calcYoY(current: number, prior: number): number {
  return ((current - prior) / Math.abs(prior)) * 100
}

export function calcQoQ(current: number, prior: number): number {
  return ((current - prior) / Math.abs(prior)) * 100
}

export function formatBillion(val: number): string {
  return val.toLocaleString('ko-KR', { maximumFractionDigits: 1 }) + '억'
}

export function formatTrillion(val: number): string {
  if (val >= 10000) {
    return (val / 10000).toFixed(1) + '조'
  }
  return val.toLocaleString('ko-KR', { maximumFractionDigits: 0 }) + '십억'
}
